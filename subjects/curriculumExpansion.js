function concept(term, simple, detailed, exam) {
  return { term, simple, detailed, exam };
}

function addGrade(subject, gradeId, units) {
  const existing = subject.grades.find((grade) => grade.id === gradeId);
  if (existing) {
    const seen = new Set(existing.units.map((unit) => unit.title));
    units.forEach((unit) => {
      if (!seen.has(unit.title)) existing.units.push(unit);
    });
    return;
  }
  subject.grades.push({ id: gradeId, name: gradeNames[gradeId], units });
}

function outline(subject, grade, entries) {
  return createOutlineUnits(subject, grade, entries);
}

