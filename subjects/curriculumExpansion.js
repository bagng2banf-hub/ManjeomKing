export function applyCurriculumExpansion(subjects) {
  subjects.forEach((subject) => {
    const outlines = existingSubjectOutlines[subject.id];
    if (!outlines) return;
    Object.entries(outlines).forEach(([grade, entries]) => {
      addGrade(subject, grade, outline(subject.id, grade, toEntries(entries)));
    });
  });

  detailedSamples.forEach((unit) => {
    const subject = subjects.find((item) => item.id === unit.subject);
    if (subject) addGrade(subject, unit.grade, [unit]);
  });

  return [...subjects, technology, information, ethics, music, art, pe];
}
